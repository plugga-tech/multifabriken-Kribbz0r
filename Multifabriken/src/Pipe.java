class Pipe {
    private int diameter;
    private int length;

    public Pipe(int diameter, int length) {
        this.diameter = diameter;
        this.length = length;
    }

    public int getDiameter() {
        return diameter;
    }

    public void setDiameter(int diameter) {
        this.diameter = diameter;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
