public class OatMilk {

    int fat;
    double cubicLiters;

    public OatMilk(int fat, double cubicLiters) {
        this.fat = fat;
        this.cubicLiters = cubicLiters;

    }

    public int getFat() {
        return fat;
    }

    public void setFat(int fat) {
        this.fat = fat;
    }

    public double getCubicLiters() {
        return cubicLiters;
    }

    public void setCubicLiters(double cubicLiters) {
        this.cubicLiters = cubicLiters;
    }
}
