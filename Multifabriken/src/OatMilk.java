class OatMilk {

    private double fat;
    private double cubicLiters;

    public OatMilk(double fat, double cubicLiters) {
        this.fat = fat;
        this.cubicLiters = cubicLiters;
    }

    public double getFat() {
        return fat;
    }

    public double getCubicLiters() {
        return cubicLiters;
    }

}
